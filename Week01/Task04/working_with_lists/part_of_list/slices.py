locations = ['Paris', 'London', 'New York', 'Tokyo', 'Moscow', 'Berlin']
print("The first three items in the list are:", locations[:3])  
# The first three items in the list are: ['Paris', 'London', 'New York']

print("Three items from the middle of the list are:", locations[2:5])
# Three items from the middle of the list are: ['New York', 'Tokyo', 'Moscow']

print("The last three items in the list are:", locations[-3:])
# The last three items in the list are: ['Tokyo', 'Moscow', 'Berlin']